            <h3 class="h3">Asociere străveche</h3>

            <p class="paragraf">
                <span class="alineat">An de an</span>, la început de mai, la Rudăria, în Banatul Montan, are loc <i>Sâmbra oilor</i>. Sărbătoarea se mai numește <i>Smâlţul</i> sau <i>Măsuratul oilor</i> (primul muls al oilor de când au fost urcate la munte) şi marchează <b>începutul anului pastoral</b>. În această zonă se păstrează o veche formă de asociere între ciobani, iar <i>Smâlţul</i> are menirea de a indica, peste vară, cât lapte i se cuvine fiecăruia din producţia întregii turme.
            </p>

            <h3 class="h3">Cu mic, cu mare, la stână</h3>

            <p class="paragraf">
                <span class="alineat">Dimineaţa</span> în zori, tineri şi bătrâni părăsesc satul pentru a urca la stâna din vârful muntelui, unde are loc sărbătoarea. În două-trei camioane încape aproape toată suflarea satului. Urcuşul, pe un drum sălbătic de o frumuseţe rară, cu liliac roz de o parte şi de alta, durează aproape o oră. Alţii aleg să urce pe jos, alţii călare, iar alţii, veniţi de la oraş, cu maşinile de teren. Satul rămâne cufundat în linişte: doar cei foarte bătrâni şi bolnavii nu urcă pe munte.
            </p>

            <p class="paragraf">
                <span class="alineat">La stână</span>, ciobanii au pregătit focul cu o seară înainte, pentru ca dimineaţa jarul să fie încins, numai bun pentru mieii ce vor fi puşi la frigare.
            </p>

