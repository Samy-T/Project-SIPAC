			<h1>Morile de apă</h1>
				
            <div style="text-align: center;"><img src="images/Moara Îndărătnica.jpg" class="imageObiective" title="Moara Îndărătnica dintre râuri" alt="Nu se poate afișa această imagine."/></div>
				
            <p class="paragraf">
                <span class="alineat">Morile</span> din satul Eftimie Murgu (Rudăria), cel mai mare muzeu în aer liber (96 ha), sufletul truditorilor pământului, în număr de 8 în anul 1772, 51 în anul 1874 și 22 în prezent, râșnițe primordiale al timpului, macină astăzi făina secolului al XXI-lea legând și dezlegând prin descântece, destinele și poveștile unui spațiu întinerit și etern, despre care se va mai vorbi.
            </p>
                
            <p class="paragraf">
                <span class="alineat">Așezate</span> asemenea unor salbe de mărgele de-a lungul cursului apei, pe o lungime de 3 km în sat și în afara satului, morile valorifică potențialul energetic scăzut al râului Rudărica.
            </p>
                
            <div style="text-align: center;">
                <i><span style="font-size: large; ">Harta complexului:</span></i><br><br>
                <img src="images/harta_mori.jpg" class="imageObiective" title="Harta Complexului de mori cu apă de la Rudăria" alt="Nu se poate afișa această imagine."/>
            </div>
				
            <p class="paragraf">
                <span class="alineat">Exploatând</span> la maximum avantajele terenului, corectându-l prin tunele și baraje, țăranii rudăreni au construit, an dupa an și veac dupa veac, un <u>complex mulinologic</u> monumental și pitoresc, ingenios și eficient, capabil să rezolve nevoile de măcinare ale celor câteva sute de familii din momentul maximei expansiuni a localitații.
            </p>
                
            <p class="paragraf">
                <span class="alineat">Roțile</span> acționate de forța apei, inima morii, erau confecționate la început din lemn, iar apoi au fost înlocuite cu cele de metal, pe motive de durabilitate. Lagărul care susține mișcarea de rotație a rămas tot din cremene alb și este cea mai sigură soluție, pentru că aici nu pot rezista rulmenții.
            </p>
                
            <div style="text-align: center;">
                <img src="images/mecanismul-morii.jpg" class="imageObiective" title="Mecanismul morilor de la Rudăria" alt="Nu se poate afișa această imagine."/>
            </div>
				
            <p class="paragraf">
                <span class="alineat">Făina</span> produsă aici are un gust inconfundabil. Bobul de porumb este măcinat de pietre cu o viteză mică, față de viteza morilor de curent.
            </p>
                
            <div style="text-align: center;">
                <img src="images/Făină.jpg" class="imageObiective" title="Făină" alt="Nu se poate afișa această imagine."/>
            </div>
                
            <p class="paragraf">
                <span class="alineat">La</span> început, moara era proprietatea a până la 16-20 de familii care au contribuit la construirea ei. Pe baza unei înțelegeri nescrise, fiecare familie are dreptul la un anumit timp de măcinat. Acest “rând la moară” se poate vinde sau se poate moșteni. O dată pe lună, o zi este rezervată întreținerii morii, aici fiecare familie cu rând își trimite un membru din familie, altfel riscând să-și piardă rândul.
            </p>
                
            <p class="paragraf">
                <span class="alineat">Morile</span> poartă numele constructorului, numele locului unde este amplasată sau numele unei “familii șef”. Astfel întalnim nume ca: Moara cu Tunel, Roșoanea, Viloanea, Trăiloanea, Îndărătnica dintre râuri și Îndărătnica de la perete, Firizoanea, Popeasca, Hambăroanea, Maxinoanea, Vamulea, Brusoanea, Lăiața, Moara din Țarină, Podul Ilochii, Firiz, Micloșoanea, Băniana, Prundulea, Bâțolea, Pățoanea etc.
            </p>
					
            <p class="paragraf">
                <span class="alineat">Se</span> spune că morile de apă sunt păzite de <i>"Adam şi Eva</i>", două stânci cu înfăţişare omenească, care se află undeva sus pe Cheile Rudăricăi, spre ieşirea din rezervaţia mulinologică. <i>Pe vremea dacilor, erau doi tineri care se iubeau, însă părinţii lor nu se prea înţelegeau, tocmai din cauza unei mori. Ei le-au interzis tinerilor să se mai vadă, iar cei doi au fugit în munţi. Atunci, toate rudele au plecat să-i caute, însă ca să li se piardă urma, îndrăgostiţii au urcat pe stânci şi au fost loviţi de un trăsnet. Cei doi s-au transformat în stânci şi de atunci păzesc zona.</i> Se spune că locul este total inaccesibil şi învelit de mister. Niciun alpinist nu a reuşit să urce acolo, carabinele se rup, corzile se încurcă sau din senin începe să plouă cu tunete şi fulgere.
            </p>
                
            <div style="text-align: center;">
                <img src="images/Adam și Eva.jpg" class="imageObiective"title="Adam și Eva" alt="Nu se poate afișa această imagine."/>
            </div>
                
            <p class="paragraf">
                <span class="alineat">Pe</span> fondul transformărilor postrevoluționare ( 1989 ), mai multe mori au fost lăsate să cadă în paragină, amenințând astfel întregul sit mulinologic. În sprijinul acestui muzeu în aer liber au sărit cei de la Muzeul Astra din Sibiu, care, contactând fonduri europene, au recondiționat morile prin programul Euroart.
            </p>
                
