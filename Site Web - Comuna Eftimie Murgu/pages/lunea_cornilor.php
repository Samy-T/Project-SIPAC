            <h1>Lunea cornilor</h1>

            <p class="paragraf">
                <span class="alineat">Festivitatea populară</span> <i>„Lunea Cornilor”</i>, cu origini străvechi, este un obicei cu funcţie socială bine definită, care are loc în fiecare an aproape de venirea primăverii şi se desfăşoară în ziua de luni, la începutul postului Paştelui. Ea este aşteptată cu nerăbdare şi mare bucurie de toată suflarea comunităţii din sat.
            </p>

            <div style="text-align: center;">
                <img src="images/lunea cornilor.jpg" class="imageInfo" width="765" height="500" title="Ziua cornilor" alt="Această imagine nu poate fi afișată!"/>
            </div>
                
            <p class="paragraf">
                <span class="alineat">Oamenii</span> mascaţi reprezentau spiritele rele, malefice pe care oamenii le alungă, pentru a putea sosi un an mai bun. Toate popoarele vechi sărbătoreau anul nou, primăvara, când începea să prindă viaţă natura.
                Alaiul de măşti şi Jocul cornilor (maimuci) este practicat la o sărbătoare care celebrează înflorirea cornului, un arbust ce ocupă un loc special în culturile agro-pastorale, fiind un vestitor al schimbării de anotimp.
            </p>

            <div style="text-align: center;">
                <img src="images/scoruș.jpg" class="imageInfo" width="765" height="550" title="Scoruș sau corn" alt="Această imagine nu poate fi afișată!"/>
            </div>
                
            <p class="paragraf">
                <span class="alineat">Cu mult timp în urmă</span>, nu era ceva organizat, ci totul era spontan. Oamenii mascaţi mergeau prin sat făcând giumbuşlucuri (glume), urmaţi de muzică (fanfară, taraf), iar populaţia adunată pe uliţele satului îi alunga simbolizând alungarea spiritelor rele.
                Pe parcurs <i>„Lunea cornilor”</i> a devenit regizată. Aici, mascaţii se mai numesc şi maimozi, iar în celelalte sate li se spun corni, ori maimuci. Luni, nunta cornilor are o participare mai mare şi ca orice nuntă are miri, naşi şi nuntaşi. La fel ca la o nuntă, mireasa împarte daruri la naşi, socri, rudele apropiate şi pentru toţi sătenii.
            </p>
				
            <p class="paragraf">
                <span class="alineat">Darurile</span> sunt, de fapt, strigături şi obiecte prin care se satirizează anumite persoane pentru faptele pe care le-au făcut în cadrul comunităţii, în politică sau în administraţie. Ele sunt prezentate foarte subtil pentru a nu se crea duşmănie şi adesea pot fi înţelese doar de către cei cărora le sunt adresate, dar nimeni nu se supără.
                Darurile de la „Lunea cornilor” erau şi sunt strigate de persoane care au un pic de talent şi erau oameni de bază în sat. Pentru ascultarea darurilor se adună tot satul. Vin şi mulţi străini. Strigăturile aveau drept scop îndreptarea unor manifestări şi apucături în comunitate. Scrisorile cu daruri erau introduse de oameni în prealabil într-o cutie metalică sudată într-un stâlp de beton. Ele nu puteau fi prezentate aşa cum erau scrise, de aceea o comisie le aranja şi le stiliza pentru a putea fi prezentate publicului.
            </p>
                
            <p class="paragraf">
                <span class="alineat">Exemplu</span> de daruri strigate:
